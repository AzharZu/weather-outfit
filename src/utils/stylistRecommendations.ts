export const getStylistRecommendation = (
  weather: WeatherData,
  user: User,
  mood?: MoodType
): StylistRecommendation => {
  const { temperature } = weather;
  const { gender, style } = user;
  
  const isFemale = gender === 'female';
  const isElegant = style === 'elegant';
  
  // –û–ø—Ä–µ–¥–µ–ª—è–µ–º –±–∞–∑–æ–≤—ã–π —Å—Ç–∏–ª—å –ø–æ –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∏—é –∏ –ø–æ–≥–æ–¥–µ
  if (mood === 'business-lady') {
    return getBusinessLadyLook(temperature, isFemale, weather);
  } else if (mood === 'cozy-coffee') {
    return getCozyCoffeeLook(temperature, isFemale, weather);
  } else if (mood === 'parisian-elegance') {
    return getParisianLook(temperature, isFemale, weather);
  } else if (mood === 'rainy-cinema') {
    return getRainyCinemaLook(temperature, isFemale, weather);
  } else if (mood === 'street-confident') {
    return getStreetConfidentLook(temperature, isFemale, weather);
  } else if (mood === 'romantic-dreamy') {
    return getRomanticLook(temperature, isFemale, weather);
  }
  
  // –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–π –≤—ã–±–æ—Ä –ø–æ –ø–æ–≥–æ–¥–µ –∏ —Å—Ç–∏–ª—é
  return getWeatherBasedLook(temperature, isElegant, isFemale, weather);
};

